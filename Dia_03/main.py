# Gere uma lista com todos os números entre 1 e 200 que sejam divisíveis por 3 ou 5, mas não por ambos.  #
# ====================================================================================================== #

l = [i for i in range(1, 201) if (i%3 == 0) ^ (i%5 == 0)]
print(l)

